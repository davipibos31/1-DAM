using System;

namespace ejercicio_2._2._5._2
{
    class Program
    {
        static void Main(string[] args)
        {
            int numero;
            
            numero = 1;
            while (numero <= 5)
            {
                Console.Write(numero);
                numero++;
            }

            numero = 1;
            while (numero <= 5)
            {
                Console.Write(numero);
                numero++;
            }

            numero = 1;
            while (numero <= 5)
            {
                Console.Write(numero);
                numero++;
            }
            numero = 1;

            while (numero <= 5)
            {
                Console.Write(numero);
                numero++;
            }
        }
    }
}
